function filterByGenre(genre) {
  const cards = document.querySelectorAll(".card");
  cards.forEach((card)=>{
    const cardCategory = (card.dataset.genre).toLowerCase();  /* || "" */
if (cardCategory === (genre).toLowerCase()) {/*  || "" */
      card.style.display = "";
    } else {
      card.style.display = "none";
    }
  });
}
